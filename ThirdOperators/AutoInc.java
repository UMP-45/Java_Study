// 自动递增和递减

public class AutoInc {
  public static void main(String[] args){
    int i = 0;
    System.out.println("i : " + i);
    System.out.println("++i : " + ++i);
    System.out.println("i++ : " + i++);
    System.out.println("i : " + i);
    System.out.println("--i : " + --i);
    System.out.println("i-- : " + i--);
    System.out.println("i : " + i);
  }
}
